package com.example.saicm.riverraid;

public class Airplane extends Obstacle {

    public Airplane(int x, int y) {
        super(x, y);
        image = R.drawable.airplane;
    }

    @Override
    public int getX() {
        return super.getX();
    }

    @Override
    public int getY() {
        return super.getY();
    }

    @Override
    public void setX(int x) {
        super.setX(x);
    }

    @Override
    public void setY(int y) {
        super.setY(y);
    }

    @Override
    public int getImage() {
        return super.getImage();
    }

    @Override
    public void setImage(int image) {
        super.setImage(image);
    }
}
